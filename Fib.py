#Раз формула нахождения n-го числа Фибонначи: F(n) = F(n-1)+ F(n-2), где n=> 2, то создадим соответствующую рекурсию
print("Здравствуйте")
n = int(input("Введите номер числа Фибонначи, которое вы хотели бы узнать: "))
def fib(n):
    if n == 0:
        return 0
    elif n == 1:
        return 1
    else:
        return fib(n-1) + fib(n-2)
print(fib(n),"-ваше число")
